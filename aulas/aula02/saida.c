#include <stdio.h> //inclusao de bibliotecas da linguagem
#include <math.h>

int main () {
    int n  = 45.78;
    printf("o coseno de %d é %f\n",n, cos(n)); //função coseno de um numero inteiro n
    printf("o seno de %d é %f\n",n, sin(n)); //função seno de um numero inteiro n
    printf("o tangente de %d é %f\n",n, tan(n)); //função tangente de um numero inteiro n

    return 0;

}